<p>Para <b>A</b>: </p>
<p><b>ID</b> = B en 1ra, 2da y 3ra opción.</p>
<p><b>II</b> = C en 1ra opción de cada una de las B.</p>
<p><b>IIA</b> = C en 1ra, 2da y 3ra opción de cada una de las B.</p>
