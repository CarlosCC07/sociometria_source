			<h3>Análisis sociométrico</h3>
			<div class="contentHelp">	
		<p>El Estudio Sociométrico es una medición en donde las personas eligen  compañeros de cualquier nivel organizacional, permitiéndonos determinar diferentes  estilos de liderazgo.</p><br>

<p>Algunos son :</p><br>

<b>1)	Liderazgo de Afinidad</b><br><br>
El líder por Afinidad es aquella persona con quien les gustaría trabajar a un determinado grupo. Su papel es importante para organizar trabajos de equipo, comisiones o mejorar las condiciones de operación de un negocio al estar el Líder al frente de una o varias actividades claves.
Este estilo es el que permite que se configuren “grupos de trabajo” tales como : <br><br>
<ul>
	<li>Círculos de Calidad / ISO</li>
	<li>Equipos de Seguridad e Higiene</li>
	<li>Comisiones de Atención a la Comunidad</li>
	<li>Comisiones de Empleados (Secretariales )</li>
</ul>

<b>2)	Liderazgo por Ascendencia</b><br><br>
En esta expresión nos encontramos con aquellas personas que son respetadas por un grupo  -son Líderes políticos – y  cuya influencia es decisiva en los momentos de conflicto. En situaciones ordinarias son los orientadores del grupo.
	Este estilo es el que permite que se organicen o se determinen conductas de un grupo, tales como: <br><br>
	<ul>
		<li>Comité Sindical</li>
		<li>Comisión de Valores o Código de Conducta</li>
		<li>Comité de Valuación Técnica (Tecnología , Prácticas nuevas, etc) </li>
		<li>Grupos de Administración de Fondos (Caja de Ahorro, Seguros, etc)</li>
	</ul>


<b>3)	Liderazgo por Popularidad</b><br><br>
Aquel compañero simpático, “el que cae bien”, “el alma de las reuniones o las  fiestas”; puede también atenuar o minimizar situaciones de conflicto al asumir en estos momentos la dirección del grupo con objetivos que pretendan diluir o distraer las tensiones de los grupos.<br>

Este estilo de liderazgo es útil para papeles de integración del negocio, tales como: <br><br>
<ul>
	<li>Organización de fiestas (Navidad, Día de las Madres, etc.)</li>
	<li>Promoción de Actividades Deportivas y/o Culturales</li>
	<li>Comité calificador en eventos sociales / familiares</li>
</ul>


<p><br>
En este estudio observamos quienes son los líderes y quienes los eligen, pero es muy importante también saber a quienes eligen los líderes, mismos que denominaremos <b>“Lideres Ocultos”</b>.
Además existen ciertos líderes ocultos a quienes denominamos <b>”Líderes ajenos”</b>, son personas escogidas por los <b>“Líderes informales”</b> pero que no pertenecen al grupo,  ya sea de las áreas o niveles preseleccionados.
Tenemos por otro lado una lista de personas excluidas socialmente de los grupos, es decir aquellos que no son elegidos por nadie, en ninguna de las 9 (nueve) alternativas que el personal encuestado tuvo oportunidad de elegir.
La influencia de estas personas no es determinante por lo que la Organización no debe delegar en ellos papeles que deben ser desempeñados por auténticos Líderes informales y hasta por Líderes Formales.
Por último, éste modelo Sociométrico evalúa el grado de influencia por cada tipo de respuesta (calidad de la respuesta en 1ra. Opción) así como la cantidad de electores.</p><br>
