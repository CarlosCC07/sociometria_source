<h4>Simbología</h4><br>
<p> (1)	<b>#=</b> IDENTIFICADOR ASIGNADO POR LA EMPRESA PARA CONSULTA.</p>

<p>(2)	<b>IIA</b> SIGNIFICA INFLUENCIA INDIRECTA AMPLIADA</p>
<p>		2.1 LA CANTIDAD DE SEGUIDORES QUE LO ELIGEN INDIRECTAMENTE AL LIDER, EN 1RA. 2DA. Y 3RA. OPCION</p>

<p>(3)	<b>ID</b> SIGNIFICA INFLUENCIA DIRECTA</p>

<p>		3.1 LA CANTIDAD DE SEGUIDORES QUE LO ELIGEN DIRECTAMENTE , INDEPENDIENTEMENTE DE LA CALIDAD DE LA ELECCION EN 1RA. 2DA. Y 3RA. OPCION</p>

<p>(4)	<b>II</b> SIGNIFICA INFLUENCIA INDIRECTA</p>

<p>		4.1 LA CANTIDAD DE SEGUIDORES DE LOS QUE ELIGEN INDIRECTAMENTE AL LIDER, TIPICAMENTE EN 1RA. OPCION</p><br>
