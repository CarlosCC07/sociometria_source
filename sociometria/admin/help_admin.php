		<h3>Análisis Sociométrico de empresa</h3>
		<div class="contentHelp">
		<p>Para cada una de las empresas se pueden generar varios análisis sociométricos aunque solamente se permite hacer un análisis por año.
		Para ver los análisis de una empresa se debe dar primero clic en el menú a empresas y después en la lupa que aparece en la empresa escogida.</p>
		<div align="center">
				<img src="img/admin_help/go_analisis.png">
		</div>
		<br><br>
		<p>Inmediatamente se mostrará la pantalla con una tabla de los análisis de esa empresa, en caso de no haber ningún análisis se puede agregar uno dando
			clic en el botón de "Nuevo análisis"</p>
		<div align="center">
			<img src="img/admin_help/new_analisis.png">
		</div>
		<br><br>
		<p>En la siguiente pantalla se debe llenar la forma para iniciar un nuevo análisis y que se guarda en la base de datos:</p>
		<ul>
			<li><b>Nombre de plantas:</b>En este campo se deben ingresar el o los nombres de las plantas a analizar separados por comas y sin espacios entre cada nombre.</li>
			<li><b>Año correspondiente:</b>En este campo se debe escoger el año al que pertenece, no importa el mes.</li>
			<li><b>Base de datos de encuesta:</b>En este campo se debe ingresar la base de datos de la encuesta con un formato que se describirá a continuación.</li>
			<li><b>Base de datos del personal:</b>En este campo se debe ingresar la base de datos del personal de la empresa con un formato que se describirá a continuación.</li>
			<li><b>Diferenciadores de personal:</b>En este campo se debe ingresar los tipos de personal que hay en la empresa separados por comas y sin espacios, se muestra una vista previa de cuál tiene que ser su numero en la base de datos de .csv</li>

		</ul>
		<div align="center">
			<img src="img/admin_help/analisis_upload.png">
		</div><br><br>

		<p><b>Nota:</b> Para cada una de las plantas se debe tomar en cuenta que van a tener asigado un número para mantener un control entre la información, es decir, si escribes: Monterrey,Veracruz.
		A la planta de Monterrey se le asigna el número 0 y ala planta de  Veracruz el número 1. En los campos de las bases de datos siguientes se deben colocar esos números de acuerdo a la planta a la que pertenezca.
		En caso de ser solo una planta su número sería el 0 y en caso de ser más de 2 serían 2,3,4,5... y asi sucesivamente.
		Para el caso de los diferenciadores, el número que se le asigna a la vista previa ese el que se debe usar en la base de datos de personas.
		</p>

		<p>Para poder subir la información de cualquier base de datos es necesario que este en formato de Windows Comma Separated Values (.CSV)</p>
		<p>Para que el sistema funcione perfectamente es necesario realizar la conversión de .xlsx a .csv. Esto se logra en el menú de excel ir a 
		   Archivo->Guardar Como  y en Formato seleccionar Windows Comma Separated Values. Seleccionar guardar y nos pondrá un aviso, hay que seleccionar continuar y de esa manera ya estará guardado
		 en el formato que admite el sistema.</p>
		 <p><b>¡Importante!</b> Si se desea realizar un cambio a la información de alguna de las bases de datos es necesario hacerlo desde el excel ya que de lo contrario no funcionará
		  si hacemos cambios en el .csv. Al terminar el cambio es necesario repetir el proceso de conversión a .csv.</p>
		
		<p><b>Formato de base de datos encuesta</b></p>
		<ul>
			<li><b>Columna A:</b> Folio de la encuesta, en caso de no tener dejar en blanco</li>
			<li><b>Columna B:</b> Nombre de la persona que contestó la encuesta</li>
			<li><b>Columna C:</b> Departamento que puso la persona que contestó la encuesta</li>
			<li><b>Columna D:</b> Número de planta definida anteriormente</li>
			<li><b>Columna E:</b> Nombre puesto de ascendencia en 1er lugar</li>
			<li><b>Columna F:</b> Nombre puesto de ascendencia en 2do lugar</li>
			<li><b>Columna G:</b> Nombre puesto de ascendencia en 3er lugar</li>
			<li><b>Columna H:</b> Nombre puesto de afinidad en 1er lugar</li>
			<li><b>Columna I:</b> Nombre puesto de afinidad en 2do lugar</li>
			<li><b>Columna J:</b> Nombre puesto de afinidad en 3er lugar</li>
			<li><b>Columna K:</b> Nombre puesto de popularidad en 1er lugar</li>
			<li><b>Columna L:</b> Nombre puesto de popularidad en 2do lugar</li>
			<li><b>Columna M:</b> Nombre puesto de popularidad en 3er lugar</li>
		</ul>
		<div align="center">
			<img src="img/admin_help/survey_bd.png">
		</div><br><br>

		<p><b>Formato de base de datos personal</b></p>
		<ul>
			<li><b>Columna A:</b> Número identificador del empleado</li>
			<li><b>Columna B:</b> Nombre del empleado</li>
			<li><b>Columna C:</b> Fecha de ingreso (MM/DD/AA)</li>
			<li><b>Columna D:</b> Departamento del empleado</li>
			<li><b>Columna E:</b> Tipo de trabajador (Empleado,Sindicalizado, Honorarios) en número</li>
			<li><b>Columna F:</b> Si es clave, clave2, Directivo, Gerente en número</li>
			<li><b>Columna G:</b> Planta a la que pertenece, en número descrito anteriormente</li>
		</ul>
		<div align="center">
			<img src="img/admin_help/employees_bd.png">
		</div><br><br>

		<p>Al tener los formatos listos y la información colocada en la forma del análisis, al dar clic en "Subir Información", debe aparecer una pantalla
			actualizada de los análisis con los siguientes campos:</p>
		<ul>
			<li><b>Año:</b> Año del análisis</li>
			<li><b>Plantas:</b> Plantas del análisis</li>
			<li><b>Usuario:</b> Usuario que generó el análisis por última vez</li>
			<li><b>Personal:</b> Tipo de personal que se incluye para el análisis</li>
			<li><b>Fecha de último análisis: Fecha del último análisis</b></li>
			<li><b>Ver información de la encuesta:</b>En esta sección se encuentra información detallada de la encuesta. (Tiempo, cantidad de encuestas, % Bateo, etc.)</li>
			<li><b>Ver duplicados (Nombre encuesta):</b> Al momento de que se encuentra terminado el análisis, se pueden ver las personas duplicadas en nombre de la encuesta.</li>
			<li><b>Ver no reconocidos (Nombre encuesta):</b>Al momento de que se encuentra terminado el análisis, se pueden ver las personas no reconocidas en nombre de la encuesta</li>
			<li><b>Ver no reconocidos (Encuesta):</b>Al momento de que se encuentra terminado el análisis, se pueden ver las personas escritas como opciones de afinidad o ascendencia o popularidad de las encuestas.</li>
			<li><b>Base de datos:</b> Este campo muestra 2 estados: Verde->Base de datos subida, Rojo->Base de datos no subida, Si está en rojo podemos cargar una dando clic en ese campo</li>
			<li><b>Estado Análisis:</b>Este campo maneja 3 estados: Verde->Análisis Finalizado, Rojo-> Análisis No iniciado, Amarillo-> Análisis Iniciado pero no terminado.</li>
			<li><b>Limpiar:</b> Esta opción sirve para borrar las bases de datos y subir nuevamente para iniciar nuevamente el análisis.</li>
			<li><b>Quitar Análisis:</b>Esta opción borra el análisis siempre y cuando se limpie primero.</li>			
		</ul>
